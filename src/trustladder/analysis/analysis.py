def run_ap7_check(workspace, cfg, log=print):
    p = ws_paths(workspace)
    exclusions = load_exclusions(p["ledger"])
    excluded_ids = {e["run_id"] for e in exclusions}
    problems = []

    denom_sources = []
    for fname in ("validity_verdict.json", "confirmatory_h1.json"):
        f = p["analysis"] / fname
        if f.exists():
            data = json.loads(f.read_text())
            denom_sources.append((fname, set(data.get("denominator_run_ids", []))))
    if not denom_sources:
        raise AP7ConsistencyError(
            "[AP7 CHECK REFUSAL] no emitted denominators found under %s; "
            "run the pipeline first." % p["analysis"])

    for fname, denom in denom_sources:           # Side A
        leak = sorted(excluded_ids & denom)
        if leak:
            problems.append("side A: excluded run(s) %s present in %s "
                            "denominator_run_ids" % (leak, fname))

    sec = p["analysis"] / "secondaries_exploratory.json"  # Side B
    honesty_ids, prov_ids = set(), set()
    if sec.exists():
        t = json.loads(sec.read_text())["tables"]["M3_honesty_ledger"]
        honesty_ids = {x["run_id"] for x in t["ap7_excluded_runs"]}
    for v in load_violations(p["ledger"]):
        if v["class"] == "PROVENANCE":
            prov_ids.add(v["run_id"])
    for rid in sorted(excluded_ids):
        if rid not in honesty_ids:
            problems.append("side B: excluded run %s ABSENT from honesty-ledger "
                            "output (dual routing violated)" % rid)
        if rid not in prov_ids:
            problems.append("side B: excluded run %s has no PROVENANCE row in "
                            "ledger violations (Sec. 6 taxonomy anchor: "
                            "confirmed contamination classifies PROVENANCE)" % rid)

    if problems:
        raise AP7ConsistencyError(
            "[AP7 CONSISTENCY FAIL] " + "; ".join(problems))
    log("[AP7] consistency check PASS (both sides): %d exclusion(s) out of "
        "all denominators and present in honesty output with PROVENANCE rows"
        % len(excluded_ids))
    return True


