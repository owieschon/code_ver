# AP7 BOTH-SIDES CONSISTENCY CHECK (analysis side)
# Side A: no excluded run_id appears in any emitted denominator.
# Side B: every excluded run_id appears in the honesty-ledger output
#         AND carries a PROVENANCE row in the ledger violations.
# (The ledger-side check lives in the ledger component.)
