def extract_verbatim_from_v3(constant, v3_path=PREREG_V3):
    """Locate the constant's first line in the registered v3 text and
    return exactly that many source lines (for the diff receipt)."""
    src = Path(v3_path).read_text().splitlines()
    want = constant.splitlines()
    for i, line in enumerate(src):
        if line == want[0]:
            return "\n".join(src[i:i + len(want)])
    raise VerbatimError("anchor line not found in v3: %r" % want[0])


def verify_verbatim(v3_path=PREREG_V3, mutate=False, log=print):
    """LIVE CHECK: embedded readout constants must be byte-identical
    to the registered v3 text. --mutate demonstrates red by flipping
    one character of the candidate before comparison."""
    if not Path(v3_path).exists():
        raise RefusalError(
            "verify-verbatim and readouts require the registered preregistration "
            "(%s), which is kept private and is not shipped in this public copy "
            "(see dev-history/README.md). The statistical pipeline (validity, "
            "confirmatory, secondaries, ap7-check) runs without it."
            % Path(v3_path).name)
    blocks = {
        "READOUT_LIMITS (Sec. 8A)": VERBATIM_READOUT_LIMITS,
        "CLAIM_LIMITS_ADDENDA (Sec. 1, Amendment G)": VERBATIM_CLAIM_LIMITS,
        "H-ENT6_BINDING_CAVEAT (Sec. 8A)": VERBATIM_ENT6_CAVEAT,
    }
    failures = []
    for name, const in blocks.items():
        candidate = const
        if mutate:
            candidate = const.replace("no", "NO", 1)  # single deliberate flip
        source = extract_verbatim_from_v3(const, v3_path)
        if candidate != source:
            diff = "\n".join(difflib.unified_diff(
                source.splitlines(), candidate.splitlines(),
                fromfile="PREREGISTRATION_v3 (registered)",
                tofile="embedded template constant", lineterm=""))
            failures.append("[VERBATIM FAIL] %s diverges from v3:\n%s" % (name, diff))
        else:
            log("[VERBATIM] %s: byte-identical to v3 (%d lines)"
                % (name, len(const.splitlines())))
    if failures:
        raise VerbatimError("\n".join(failures))
    return True


def generate_readouts(out_dir, log=print):
    verify_verbatim(log=log)        # templates only ship if verbatim holds
