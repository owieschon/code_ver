def run_all(workspace, cfg, log=print):
    log("[PIPELINE] AP6 structural order: validity -> confirmatory -> "
        "secondaries -> AP7 check -> readouts")
    run_validity(workspace, cfg, log=log)        # 1. writes verdict
    run_confirmatory(workspace, cfg, log=log)    # 2. refuses unless VALID
    run_secondaries(workspace, cfg, log=log)     # 3. exploratory only
    run_ap7_check(workspace, cfg, log=log)       # 4. both-sides routing
    if PREREG_V3.exists():
        generate_readouts(ws_paths(workspace)["analysis"] / "readouts", log=log)
    else:
        log("[READOUTS] skipped: require the registered preregistration, which "
            "is kept private and not shipped in this public copy (see "
            "dev-history/). The statistical core above is complete.")
    log("[PIPELINE] complete")


def main(argv=None):
    ap = argparse.ArgumentParser(description="TrustLadder Stage-1 frozen analysis")
    ap.add_argument("command", choices=[
        "validity", "confirmatory", "secondaries", "ap7-check", "all",
        "readouts", "verify-verbatim", "matrix-guard-demo", "dummy"])
    ap.add_argument("--workspace", default=str(HARNESS),
                    help="root holding records/, ledger/, analysis/ "
                         "(default: harness root)")
    ap.add_argument("--config", default=str(DEFAULT_CONFIG))
    ap.add_argument("--scenario", default="confirmed",
                    choices=["confirmed", "spans_floor", "spans_extended",
                             "refuted"])
    ap.add_argument("--break", dest="break_gate", default=None,
                    choices=["vg1", "vg2", "vg3", "vg4", "vg5"],
                    help="dummy only: perturb data so the named gate fails")
    ap.add_argument("--out", default=str(HERE / "readouts"))
    ap.add_argument("--mutate", action="store_true",
                    help="verify-verbatim only: demonstrate red")
    a = ap.parse_args(argv)

    try:
        if a.command == "dummy":
            generate_dummy(a.workspace, a.scenario, a.break_gate)
            return 0
        if a.command == "matrix-guard-demo":
            matrix_guard_demo()
            return 0
        if a.command == "verify-verbatim":
            verify_verbatim(mutate=a.mutate)
            return 0
        if a.command == "readouts":
            generate_readouts(a.out)
            return 0
        cfg = load_config(a.config)
        {"validity": run_validity, "confirmatory": run_confirmatory,
         "secondaries": run_secondaries, "ap7-check": run_ap7_check,
         "all": run_all}[a.command](a.workspace, cfg)
        return 0
    except (RefusalError, ConfigError, AP7ConsistencyError,
            ExploratoryLabelError, MatrixPreservationError, VerbatimError) as e:
        print(str(e), file=sys.stderr)
        return 2


if __name__ == "__main__":
    sys.exit(main())
